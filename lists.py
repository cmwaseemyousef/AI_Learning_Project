# Creating a list
fruits = ["Apple", "Banana", "Mango", "Grapes"]

# Accessing elements
print("First fruit:", fruits[0])  # Apple
print("Last fruit:", fruits[-1])  # Grapes

# Adding an item
fruits.append("Orange")
print("Updated list:", fruits)

# Looping through a list
for fruit in fruits:
    print("I like", fruit)
